@st.cache_resource
def train_surrogate_model():
    """Trains a localized Random Forest to calculate true SHAP feature importance."""
    np.random.seed(42)
    # Simulate historical data: Temp, Rain, NDVI, Vulnerability
    X = np.random.rand(500, 4) * [40, 200, 1.0, 100] 
    # Cases driven primarily by rain and temp interactions
    y = (X[:,0]*2.5) + (X[:,1]*0.8) + (X[:,2]*120) + (X[:,3]*0.5) + np.random.normal(0, 15, 500)
    
    model = RandomForestRegressor(n_estimators=50, max_depth=5, random_state=42)
    model.fit(X, y)
    explainer = shap.Explainer(model)
    return model, explainer

rf_model, shap_explainer = train_surrogate_model()

