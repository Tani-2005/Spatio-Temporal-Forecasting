@st.cache_data(ttl=600)
def get_live_telemetry(lat, lon, geo_code):
    telemetry = {"ndvi": 0.35, "temp": 30.5, "rain": 0.0, "trends": 45} 
    try:
        from src.live_data import fetch_latest_vegetation_index, fetch_live_disease_trends
        telemetry["ndvi"] = fetch_latest_vegetation_index(lat, lon)
        weather_url = f"https://api.open-meteo.com/v1/forecast?latitude={lat}&longitude={lon}&current=temperature_2m,precipitation&timezone=auto"
        weather_res = requests.get(weather_url, timeout=5).json()
        if "current" in weather_res:
            telemetry["temp"] = weather_res["current"]["temperature_2m"]
            telemetry["rain"] = weather_res["current"]["precipitation"]
        trends_df = fetch_live_disease_trends("dengue", geo_code)
        if not trends_df.empty:
            telemetry["trends"] = int(trends_df.iloc[-1, 0]) 
    except Exception:
        pass 
    return telemetry

def generate_pdf_report(country, peak, capacity, temp, rain, status):
    """Generates a downloadable Executive SITREP."""
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", 'B', 16)
    pdf.cell(200, 10, txt=f"EXECUTIVE SITREP: {country.upper()}", ln=1, align='C')
    pdf.line(10, 22, 200, 22)
    
    pdf.set_font("Arial", '', 12)
    pdf.ln(10)
    pdf.cell(200, 10, txt=f"Date Generated: {datetime.today().strftime('%Y-%m-%d %H:%M')}", ln=1)
    pdf.cell(200, 10, txt=f"System Status: {status}", ln=1)
    
    pdf.ln(5)
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(200, 10, txt="1. Core Telemetry & Constraints", ln=1)
    pdf.set_font("Arial", '', 12)
    pdf.cell(200, 10, txt=f"   - Simulated Peak Cases: {peak}", ln=1)
    pdf.cell(200, 10, txt=f"   - National ICU Capacity: {capacity}", ln=1)
    pdf.cell(200, 10, txt=f"   - Live Env Trigger (Temp): {temp} C", ln=1)
    pdf.cell(200, 10, txt=f"   - Live Env Trigger (Rain): {rain} mm", ln=1)
    
    pdf.ln(10)
    pdf.set_font("Arial", 'I', 10)
    pdf.multi_cell(0, 10, txt="CONFIDENTIAL. Generated by the Automated Early Warning Engine. Immediate localized resource allocation is recommended if peak exceeds capacity.")
    
    # Cast the output directly to bytes for fpdf2
    return bytes(pdf.output())

